export async function POST() {
  try {
    await db.$executeRawUnsafe(`DELETE FROM "Product"`);

    for (const product of defaultProducts) {
      await db.$executeRawUnsafe(
        `INSERT INTO "Product" ("id", "name", "description", "longDescription", "price", "category", "image", "features", "badge", "isActive", "sortOrder", "createdAt", "updatedAt")
         VALUES (gen_random_uuid()::text, $1, $2, $3, $4, $5, $6, $7, $8, true, $9, NOW(), NOW())`,
        product.name, product.description, product.longDescription,
        product.price, product.category, product.image,
        product.features, product.badge, product.sortOrder,
      );
    }

    const existingSettings = await db.storeSettings.findFirst();
    if (!existingSettings) {
      await db.storeSettings.create({ data: {} });
    }

    return NextResponse.json({
      success: true,
      message: `تم إضافة ${defaultProducts.length} منتج بنجاح`,
      data: { count: defaultProducts.length },
    });
  } catch (error) {
    logger.error('Error seeding products:', error);
    return NextResponse.json({ success: false, error: 'Failed to seed products' }, { status: 500 });
  }
}
