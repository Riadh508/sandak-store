export async function POST() {
  try {
    await db.product.deleteMany({});

    // Seed all default products
    const results: Array<Awaited<ReturnType<typeof db.product.create>>> = [];
    for (const product of defaultProducts) {
      const created = await db.product.create({
        data: {
          name: product.name,
          description: product.description,
          longDescription: product.longDescription,
          price: product.price,
          category: product.category,
          image: product.image,
          features: product.features,
          badge: product.badge,
          downloadUrl: product.downloadUrl || '',
          sortOrder: product.sortOrder,
        },
      });
      results.push(created);
    }

    // Create StoreSettings if it doesn't exist
    const existingSettings = await db.storeSettings.findFirst();
    if (!existingSettings) {
      await db.storeSettings.create({
        data: {},
      });
    }

    return NextResponse.json({
      success: true,
      message: `تم إضافة ${results.length} منتج بنجاح`,
      data: { count: results.length },
    });
  } catch (error) {
    logger.error('Error seeding products:', error);
    return NextResponse.json({ success: false, error: 'Failed to seed products' }, { status: 500 });
  }
}
